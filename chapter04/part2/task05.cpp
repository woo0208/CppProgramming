#include <iostream>
using namespace std;

int main() {
	int n = 0;
	cout << "저장할 문자열의 크기를 입력하세요:";
	cin >> n;

	// 1. n개의 문자와 널 문자('\0')를 위한 1바이트 추가 할당
	char* word = new char[n + 1];
	
	// 2. 동적 할당 성공 여부 확인
	if (!word) {
		cout << "메모리를 할당할 수 없습니다.";
		return -1;
	}

	// 3. 입력 버퍼에 남은 개행 문자('\n')를 제거하여 다음 입력 오류 방지
	cin.ignore();

	cout << "문자열을 입력하시오:";

	// 4. 반복문 종료 후 인덱스 i를 사용하기 위해 for문 외부에서 선언
	int i;
	for (i = 0; i < n; i++) {
		char ch;
		cin.get(ch); // 공백을 포함한 문자 하나를 읽음

		// 사용자가 엔터(줄바꿈)를 치면 문자열 입력이 끝난 것이므로 반복 중지
		if (ch == '\n') break;

		word[i] = ch; // 입력받은 문자를 배열에 순차적으로 저장
	}

	// 5. C-스트링의 끝을 표시하는 널 문자('\0')를 수동으로 삽입
	word[i] = '\0';

	cout << "입력한 문자열은 " << word << "입니다." << endl;

	// 6. 프로그램 종료 전 동적 할당된 배열 메모리 반환
	delete[] word;

	return 0;
}
